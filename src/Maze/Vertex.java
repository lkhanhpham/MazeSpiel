package Maze;

public class Vertex {

	private int x1, x2, y1, y2, wall1, wall2; //x1, y1, wall1 = CurrentCell
											  //x2, y2, wall2 = NextCell

	public int getX1() {
		return x1;
	}

	public void setX1(int x1) {
		this.x1 = x1;
	}

	public int getX2() {
		return x2;
	}

	public void setX2(int x2) {
		this.x2 = x2;
	}

	public int getY1() {
		return y1;
	}

	public void setY1(int y1) {
		this.y1 = y1;
	}

	public int getY2() {
		return y2;
	}

	public void setY2(int y2) {
		this.y2 = y2;
	}

	public int getWall1() {
		return wall1;
	}

	public void setWall1(int wall1) {
		this.wall1 = wall1;
	}

	public int getWall2() {
		return wall2;
	}

	public void setWall2(int wall2) {
		this.wall2 = wall2;
	}
	
	//Getter und Setter
	
	
	
	
}
